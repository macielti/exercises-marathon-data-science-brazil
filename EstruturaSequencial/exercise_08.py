'''8. Faça um Programa que pergunte quanto você ganha 
    por hora e o número de horas trabalhadas no mês. Calcule e 
    mostre o total do seu salário no referido mês.
'''

val_hora = float(input("Quanto você ganha por hora: "))
num_horas = float(input("Digite o número de horas trabalhadas no mês: "))

print("Salário Total:", val_hora*num_horas, 'reais')